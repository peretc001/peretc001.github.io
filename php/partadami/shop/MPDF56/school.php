							<p style="text-align:center; font-size: 12pt;"><b>Коммерческое предложение</b></p>
							<p>ООО «КрасГрупп» является официальным поставщиком детской школьной мебели (наборы растущей трансформируемой мебели) «Дэми».</p>
							<p>Школьные парты «ДЭМИ» пользуется постоянным спросом неспроста. Парта-трансформер легко подстраиваются под рост ребенка и растет вместе с ним с первого класса и до окончания школы. Парта регулируется по высоте от 53 до 81,5 см (для детей ростом от 120 до 198 см – 2-6 группа роста).</p> 
							<p>Механизм подъема столешницы обеспечивает ступенчатую (9 положений) регулировку. Работа за наклонной поверхностью обеспечивает эргономическое положение для разгрузки позвоночника и шейного отдела, а так же раскрепощает мускулатуру спины. Наклонное положение столешницы позволяет читать и писать, держа прямо голову и туловище.</p>
							<p>Эргономичный школьный стул «Дэми» регулируется не только по высоте, но и по глубине, для того, чтобы ребенок мог равномерно распределить нагрузку на спину и бедра. Такая регулировка – незаменимая вещь для растущего организма, благодаря чему уменьшается риск появления у ребенка такого заболевания как сколиоз.</p>
